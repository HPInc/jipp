package com.hp.jipp.pdl

/** Represent 90-degree increments of rotation. */
enum class RotationUnit {
    /** 90 degrees clockwise rotation. */
    Rotate90,
    /** 180 degrees clockwise rotation. */
    Rotate180,
    /** 270 degrees clockwise rotation. */
    Rotate270
}
